function Player2Release () {
    if (Game_Player2ChargeMeter.value > 90) {
        Game_Player2ChargeMeter.value = 100
        Game_isPlayer2Ready = true
        Game_isPlayer2Charging = false
        if (controller.A.isPressed()) {
            Player2Up()
        } else if (controller.B.isPressed()) {
            Player2Down()
        } else {
            Player2Stop()
        }
    } else {
        Game_isPlayer2Charging = false
        Game_Player2.ax = 1000 * Game_GameSpeed
        Game_Player2.vx = (Reverse100(Game_Player2ChargeMeter.value) / 2 + 50) * -3 * Game_GameSpeed
        PlayJump()
    }
}
scene.onHitWall(SpriteKind.Player2, function (sprite, location) {
    if (sprite.isHittingTile(CollisionDirection.Right)) {
        effects.clearParticles(sprite)
        Game_isPlayer2Ready = true
        Game_Player2.ax = 0
        Game_Player2.vx = 0
        Game_Player2.x = 152
        Game_Player2ChargeMeter.value = 100
        if (controller.A.isPressed() && !(controller.B.isPressed())) {
            Player2Up()
        } else if (controller.B.isPressed() && !(controller.A.isPressed())) {
            Player2Down()
        } else if (controller.A.isPressed() && controller.B.isPressed()) {
            Player2Charge()
        } else {
            Player2Stop()
        }
    }
})
function Player2Charge () {
    Player2Stop()
    if (Game_isPlayer2Serving) {
        if (Game_isPlayer2ServeDown) {
            Player2ServeDown()
        } else {
            Player2ServeUp()
        }
    } else {
        animation.runImageAnimation(
        Game_Player2,
        assets.animation`Player2_Chargeb`,
        75,
        true
        )
        Game_isPlayer2Charging = true
        Game_isPlayer2Ready = false
    }
}
